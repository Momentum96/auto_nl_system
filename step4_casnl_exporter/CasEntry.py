def get_ird(pdf: pd.DataFrame, start, end, colname):
    ird = 0
    wls = CasEntry.wave_keymap
    cols = CasEntry.wave_col
    table_list = []
    for j, row in pdf.iterrows():
        for i in range(1, len(cols) - 3):
            wll = float(wls[cols[i]]) #n 
            wlr = float(wls[cols[i+1]]) #n+1 
            irdl = row[cols[i]] #n+1 
            weight = 1
            if start <= wll < end:
                e = (wlr - wll) * (irdl * weight)
                ird += e
            else:
                pass
        table_list.append([row['Date'], ird])
    df = pd.DataFrame(table_list, columns=['Date_'+colname, colname])
    return df

def convert_time(pdf: pd.DataFrame, year, month, day):
    table_list = []
    for j, row in pdf.iterrows():
        temp = row['Time']  # n+1 
        col = ""
        if temp[-2:] == "AM":
            if temp[0:2] == "12":
                col = "00" + temp[2:-3]
            elif temp[1] == ":":
                col = "0" + temp[0:-3]
            else:
                col = temp[0:-3]

        elif temp[-2:] == "PM":
            if temp[0:2] == "12":
                col = temp[0:-3]
            elif temp[1] == ":":
                col = str(int(temp[0:1]) + 12) + temp[1:-3]
            else:
                col = str(int(temp[0:2]) + 12) + temp[2:-3]
        table_list.append( year + "-" + month + "-" + day + " " +col)
        temp_pd = pd.DataFrame(table_list, columns=['datetime'])
    pdf = pd.concat([pdf, temp_pd], join='outer', axis=1)
    return pdf
